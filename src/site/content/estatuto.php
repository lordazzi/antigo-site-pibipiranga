<b>Artigo 1º</b>- A PRIMEIRA IGREJA BATISTA DO IPIRANGA, com sede na Rua Elba nº318, Moinho Velho, foro
da Comarca da Capital de São Paulo, doravante denominada Igreja, é uma associação civil de caráter religioso,
sem fins econômicos, fundada em 28 de abril de 1951, por tempo indeterminado e número ilimitado de membros.<br><br>

<b>Artigo 2º</b>- A Igreja reconhece e proclama Jesus Cristo como seu único Salvador e Senhor, acei ta a
Bíblia Sagrada como única regra de fé e prática e adota a Declaração Doutrinária da Convenção Batista
Brasileira.<br><br>

<b>Artigo 3º</b>- A Igreja tem as seguintes fi nalidades:<br>
<ul class='estatuto'>
<li>I - reunir-se regularmente, para prestar culto a Deus e proclamar a mensagem do Evangelho de Jesus Cristo;</li>
<li>II - estudar as Sagradas Escrituras, visando ao doutrinamento e à edificação espiritual de seus membros;</li>
<li>III - cultivar a comunhão, o bom relacionamento e a fraternidade cristã;</li>
<li>IV - promover, pelos meios adequados, a causa da ação social cristã;</li>
<li>V - cooperar com as igrejas fi liadas à Convenção Batista Brasileira;</li>
<li>VI - cooperar com a Convenção Batista do Estado de São Paulo, doravante denominada Convenção, e com a
Convenção Batista Brasileira, na realização de seus fins;</li>
<li>VII - promover, por todos os meios ao seu alcance, o estabelecimento do Reino de Deus no mundo.</li>
</ul><br>

<b>Artigo 4º</b>- A Igreja é autônoma e soberana em suas decisões, não estando sujeita a qualquer outra
igreja, instituição ou autoridade denominacional.<br><br>
