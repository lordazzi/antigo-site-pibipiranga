<hr class='estatuto'>

<b>Artigo 17º</b>- Ressalvadas a competência e as prerrogativas da Assembléia Geral, como poder soberano que
o é, a administração da Igreja será exercida por uma Diretoria composta de: Presidente, Primeiro Vice
Presidente, Segundo Vice Presidente, Primeiro Secretário, Segundo Secretário, Primeiro Tesoureiro e
Segundo Tesoureiro.
<br><br>
<b>Artigo 18º</b>- O mandato da Diretoria é de um ano, exceto o cargo de Presidente que será exercido pelo
Pastor Titular, por tempo indeterminado, a juízo da Assembléia Geral.
<br><br>
<b>Artigo 19º</b>- As datas para eleição e posse da Diretoria, bem como dos diretores de outros órgãos
existentes, serão fi xadas no calendário anual da Igreja.
<br><br>
<b>Artigo 20º</b>- Compete ao Presidente:
<br><br>
<ul class='estatuto'>
<li>I - superintender e supervisionar as atividades da Igreja;</li>
<li>II - convocar e presidir a Assembléia Geral;</li>
<li>III - representar a Igreja, ativa, passiva, judicial e extrajudicialmente;</li>
<li>IV - participar das reuniões de qualquer ministério ou órgão da Igreja, na qualidade de membro \"ex officio\";</li>
<li>V - assinar, com o Secretário, as atas da Assembléia Geral;</li>
<li>VI - abrir, movimentar e encerrar contas bancárias, juntamente com o Tesoureiro ou mediante procuração
a ele outorgada;</li>
<li>VII - apresentar à Assembléia Geral relatório periódico e anual das atividades da Igreja;</li>
<li>VIII - tomar decisões, juntamente com a Diretoria, nos casos comprovadamente excepcionais ou de extrema
urgência, \"ad referendum\" da Assembléia Geral;</li>
<li>IX - cumprir e fazer cumprir este Estatuto.</li>
</ul><br>

<b>Artigo 21º</b>- Compete aos Vice-Presidentes, na ordem de eleição, substituir o Presidente, nos seus
impedimentos e ausências.
<br><br>
<b>Artigo 22º</b>- Compete ao Primeiro Secretário:
<br>
<ul class='estatuto'>
<li>I - lavrar e assinar as atas da Assembléia Geral;</li>
<li>II - manter em ordem os arquivos, livros, cadastro e o fi chário do rol de membros da Igreja.</li>
</ul>
<br>
<i>Parágrafo único: Ressalvada a hipótese do inciso I, os demais encargos poderão ser exercidos por empregados
remunerados pela Igreja.</i>
<br><br>
<b>Artigo 23º</b>- Compete ao Segundo Secretário, sem prejuízo de outras responsabilidades que lhe forem
atribuídas, substituir o Primeiro Secretário nos seus impedimentos e eventuais ausências.<br><br>
<b>Artigo 24º</b>- Compete ao Primeiro Tesoureiro:<br><br>
<ul class='estatuto'>
<li>I - receber e escriturar as contribuições fi nanceiras destinadas à Igreja;</li>
<li>II - fazer os pagamentos autorizados pela Igreja;</li>
<li>III - abrir, movimentar e encerrar contas bancárias;</li>
<li>IV - elaborar e apresentar relatórios periódicos e anuais à Assembléia Geral.</li>
</ul><br>

<b>Artigo 25º</b>- Compete ao Segundo Tesoureiro auxiliar o Primeiro Tesoureiro, na execução de seu trabalho
e substitui-lo nos seus impedimentos e ausências.<br><br>
<b>Artigo 26º</b>- Nenhum membro da Diretoria receberá salário ou outra remuneração pelas atividades exercidas.<br><br>
<a name='cap6'></a>
<b>Artigo 27º</b>- A Igreja adotará um Manual Eclesiástico em que serão defi nidos a estrutura, os objetivos e
funcionamento dos diferentes ministérios e demais órgãos existentes.<br><br>

