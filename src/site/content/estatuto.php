<b class='capitulo'>CAPÍTULO IV</b>
<i class='capitulo'>DA ASSEMBLÉIA GERAL</i>
<br>

<?php capitulo(); ?>

<hr class='estatuto'>

<b>Artigo 11º</b>- A Assembléia Geral, constituída pelos membros da Igreja, é o seu poder soberano, cabendo-lhe
as seguintes atribuições:
<br><br>
<ul class='estatuto'>
<li>I - eleger e exonerar o Pastor Titular, observado o artigo 9º parágrafo primeiro;</li>
<li>II - eleger e exonerar os membros da Diretoria, bem como os diretores de outros órgãos da Igreja, observado o
artigo 9º parágrafo primeiro;</li>
<li>III - aprovar o orçamento anual;</li>
<li>IV - apreciar os relatórios periódicos e anuais da Diretoria e demais órgãos administrativos;</li>
<li>V - alienar por venda ou de outra forma, bem como onerar total ou parcialmente o patrimônio da Igreja;</li>
<li>VI - aceitar doações e legados;</li>
<li>VII - transferir a sede da Igreja;</li>
<li>VIII - decidir sobre a mudança do nome da Igreja;</li>
<li>IX - reformar o Estatuto;</li>
<li>X - deliberar sobre a dissolução da Igreja;</li>
<li>XI - tomar outras decisões, que envolvam aspectos administrativos, eclesiásticos e doutrinários;</li>
<li>XII - resolver os casos omissos neste estatuto.</li></ul>
<br>

<b>Artigo 12º</b>- A Assembléia Geral da Igreja reunir-se-á ordinária e extraordinariamente.<br><br>
<b>Artigo 13º</b>- A periodicidade da Assembléia Geral Ordinária será fi xada no calendário da Igreja, sendo a
Extraordinária convocada, quando se fi zer necessário.<br><br>
<b>Artigo 14º</b>- A Assembléia Geral será convocada pelo Presidente ou seu substituto, com antecedência mínima
de 7 ( sete) dias.<br><br>
<b>Artigo 15º</b>- A Assembléia Geral será realizada com quorum de 1/3( um terço) dos membros da Igreja, em primeira
convocação e de 1/5 (um quinto), em segunda convocação, 30 (trinta) minutos após, sendo as decisões tomadas
pelo voto favorável da maioria absoluta, exceto nas situações especiais previstas neste estatuto.<br><br>

<i>Parágrafo primeiro: Nos casos de eleição e exoneração do Pastor Titular, dos membros da Diretoria e do
Conselho Fiscal, alienação da sede e reforma do presente estatuto, o quorum será de 2/3 ( dois terços) dos
membros da Igreja, em primeira convocação, da metade e mais um, em segunda convocação, 7 ( sete) dias após,
e de 1/3 ( um terço) em terceira convocação, também 7 (sete) dias após.
<br><br>
Parágrafo segundo: As decisões sobre os assuntos a que se refere o parágrafo primeiro serão tomadas pelo voto
favorável de 2/3 (dois terços), dos presentes à Assembléia Geral.
<br><br>
Parágrafo terceiro: Para deliberar sobre a dissolução da Igreja será necessário o voto favorável de 80%
(oitenta por cento) dos seus membros, em 2 (duas) Assembléias Gerais, realizadas com intervalo de 3 (três)
meses, devendo a convocação ser feita, expressamente para esse fim, com ampla publicidade, inclusive pela
imprensa denominacional, observada a antecedência de 30 (trinta) dias para a convocação.
<br><br>
Parágrafo quarto: Em qualquer deliberação, o resultado fi nal da votação deverá ser fiel e integralmente
registrado em ata.
<br><br>
Parágrafo quinto: Na apreciação dos assuntos levados ao Plenário da Assembléia Geral, a Igreja adotará as
regras parlamentares da Convenção Batista Brasileira, podendo adaptá-las à sua realidade, se julgar necessário
fazê-lo.</i>
<br><br>
<a name='cap5'></a>
<b>Artigo 16º</b>- A Diretoria deverá acolher representação que lhe seja dirigida por um mínimo de 1/5
(um quinto) dos membros da Igreja solicitando a convocação da Assembléia Geral, para apreciar assuntos
expressos na referida representação.<br><br>
