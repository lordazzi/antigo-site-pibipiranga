ADMISSÃO, TRANSFERÊNCIA E DESLIGAMENTO</i><br>

<?php capitulo(); ?>

<hr class='estatuto'>

<b>Artigo 6º</b>- A Igreja é constituída de pessoas que professam sua fé em Jesus Cristo,como único
Salvador e Senhor, e aceitam as doutrinas bíblicas por ela defendidas e ensinadas.<br><br>

<b>Artigo 7º</b>- São considerados membros da Igreja, sem distinção de raça, sexo, profi ssão, ou
nacionalidade, as pessoas recebidas por decisão da Assembléia Geral, da forma que segue:<br><br>
<ul class='estatuto'>
<li>I - pública profissão de fé seguida de batismo;</li>
<li>II - carta de transferência de outras igrejas da mesma fé e ordem;</li>
<li>III- reconciliação, devidamente solicitada;</li>
<li>IV - aclamação, precedida de testemunho e compromisso.</li>
</ul>
<br>
<i>Parágrafo único: Casos especiais, não constantes neste artigo serão decididos pela
Igreja em Assembléia Geral.</i><br><br>

<b>Artigo 8º</b>- Perderá a condição de membro da Igreja aquele que for desligado, por decisão da
Assembléia Geral, nas seguintes hipóteses:<br><br>
<ul class='estatuto'>
<li>I - infringir os princípios éticos, morais e da boa conduta, defendidos pela Igreja, com fundamento
nas Sagradas Escrituras;</li>
<li>II - defender e professar doutrinas ou práticas que contrariem a Declaração Doutrinária da Convenção
Batista Brasileira;</li>
<li>III - ausentar-se dos cultos e deixar de participar das atividades eclesiásticas, sem justo motivo,
por mais de cento e oitenta dias consecutivos, caracterizando abandono e desinteresse pela Igreja e a obra
que realiza;</li>
<li>IV - solicitar desligamento;</li>
<li>V - transferir-se para outra igreja.</li>
</ul><br>
<i>Parágrafo primeiro: A Assembléia deliberará sobre a exclusão de qualquer membro,
mediante parecer, devidamente fundamentado, de uma Comissão Especial, por ela
eleita.<br><br>
<a name='cap3'></a>
Parágrafo segundo: Quando, de qualquer modo, o membro da Igreja se julgar
injustiçado, terá amplo direito de defesa.<br><br>
Parágrafo terceiro: Sob qualquer alegação, nenhum direito poderá ser reivindicado
por aquele que deixar de ser membro da Igreja.</i><br><br>

