<b class='capitulo'>CAPÍTULO IX</b>
<i class='capitulo'>DAS DIVERGÊNCIAS DOUTRINÁRIAS</i>
<br>

<?php capitulo(); ?>

<hr class='estatuto'>

<b>Artigo 35º</b>- Ocorrendo divergências entre os membros da Igreja, no tocante às praticas eclesiásticas e
às doutrinas batistas, como expostas na Declaração Doutrinária da Convenção Batista Brasileira, que causem
divisões, os bens patrimoniais ficarão na posse, domínio e administração do grupo que permanecer fiel às
mencionadas práticas e doutrinas, mesmo que seja constituído pela minoria.<br><br>

<i>Parágrafo único: De igual modo o nome \"PRIMEIRA IGREJA BATISTA DO IPIRANGA\" será de uso exclusivo do
grupo fiel às doutrinas batistas acima referidas, cabendo-lhe, também, as seguintes prerrogativas: </i>
<br><br>
<ul class='estatuto'>
<li>I - permanecer na posse e domínio do templo e demais imóveis, neles continuando a exercer suas atividades
espirituais, eclesiásticas e administrativas;</li>
<li>II - eleger outra Diretoria, inclusive um novo Pastor se as circunstâncias o exigirem;</li>
<li>III - exercer todos os direitos e prerrogativas previstos neste estatuto e na lei.</li>
</ul>
<br>
<b>Artigo 36º</b>- Configurada qualquer das hipóteses previstas no artigo 36, o julgamento do litígio será feito
por um Concilio Decisório, constituído de 15 (quinze) pastores indicados pela Convenção, através do seu órgão
representativo.<br><br>
<i>Parágrafo único: O Concilio Decisório será criado mediante exposição, devidamente fundamentada e encaminhada
ao órgão de representação acima referido.</i>
<br><br>
<b>Artigo 37º</b>- O processo de instrução e julgamento terá inicio, no prazo de 30 (trinta) dias, a contar da
data em que a representação chegar à Convenção.
<br><br>
<i>Parágrafo primeiro: Na sua primeira reunião o Conselho Decisório elegerá o Presidente e dois secretários
para os devidos fins.
<br><br>
Parágrafo segundo: O Concilio Decisório poderá realizar suas reuniões na sede da Igreja ou fora dela.
<br><br>
Parágrafo terceiro: As decisões do Concilio Decisório são irrecorríveis, entrando em vigor imediatamente.
<br><br>
Parágrafo quarto: O grupo que, de qualquer maneira, se opuser ao processo aqui estabelecido, será considerado
vencido, fi cando sujeito às sanções previstas neste estatuto e na lei.</i>
<br><br>
<b>Artigo 38º</b>- O Concilio Decisório terá o prazo de 60 (sessenta) dias, a contar da data em que tenha sido
constituído, a fi m de apurar os fatos e proferir a decisão final.
<br><br>
<i>Parágrafo único: No processo de apuração dos fatos e tomada de decisões, o Concilio Decisório fará o uso das
provas em direito admitidas.</i><br><br>

<b>Artigo 39º</b>- Enquanto não forem sanadas as divergências doutrinárias, o grupo infiel não poderá deliberar
sobre os seguintes assuntos:
<br><br>
<ul class='estatuto'>
<li>I - alienação por venda ou de outra forma, bem como oneração total ou parcial do patrimônio da Igreja;</li>
<li>II - desligamento de membros ou quaisquer restrições ao seus direitos individuais na Igreja;</li>
<li>III - reforma do estatuto ou qualquer outro documento normativo;</li>
<a name='cap10'></a>
<li>IV - mudança de sede;</li>
<li>V - alteração do nome da Igreja.</li>
</ul><br><br>

