<b>Artigo 9º</b>- São direitos dos membros:<br><br>
<ul class='estatuto'>
<li>I - participar das atividades da Igreja;</li>
<li>II - participar da Assembléia Geral, com direito ao uso da palavra e exercício do voto;</li>
<li>III - participar dos cultos, celebrações, eventos e demais atividades promovidas pela Igreja;</li>
<li>IV - votar e ser votado para quaisquer cargos ou funções, observada a maioridade civil, quando se tratar
de eleição da Diretoria da Igreja;</li>
<li>V - receber assistência espiritual.</li>
</ul>
<br>

<i>Parágrafo primeiro: Quando a decisão envolver aspectos legais, os votos dos membros civilmente incapazes
não serão computados, exigida orientação prévia do Presidente.<br><br>
Parágrafo segundo: A qualidade de membro da Igreja é intransferível, sob qualquer alegação.</i><br><br>

<b>Artigo 10º</b>- São deveres dos membros:
<ul class='estatuto'>
<li>I - manter uma conduta compatível com os princípios éticos, morais e espirituais de acordo com os
ensinos da Bíblia Sagrada;</li>
<li>II - exercitar os dons e talentos de que são dotados e contribuir com dízimos e ofertas, para que a
Igreja atinja seus objetivos e cumpra sua missão;</li>
<a name='cap4'></a>
<li>III - exercer, com zelo e dedicação, os cargos para os quais forem eleitos;</li>
<li>IV - observar o presente estatuto e zelar pelo seu cumprimento.</li></ul><br><br>

