<b class='capitulo'>CAPÍTULO X</b>
<i class='capitulo'>DAS DISPOSIÇÕES GERAIS</i>
<br>

<?php capitulo(); ?>

<hr class='estatuto'>

<br>
<b>Artigo 40º</b>- Os membros da Igreja não respondem individual, solidária ou subsidiariamente, pelas
obrigações por ela contraídas, bem como, reciprocamente, a Igreja não responde pelas obrigações assumidas
por seus membros.<br><br>

<i>Parágrafo único: Não haverá solidariedade da Igreja quanto ás obrigações contraídas por outras igrejas ou
instituições denominacionais.</i><br>

<b>Artigo 41º</b>- A Igreja não concederá avais ou fi anças, nem assumirá quaisquer obrigações estranhas às suas
finalidades.<br><br>
<b>Artigo 42º</b>- A Igreja só poderá ser dissolvida pela Assembléia Geral quando não estiver cumprindo,
reconhecidamente, as suas fi nalidades, observado o disposto nos artigos 2º e 3º deste Estatuto.<br><br>
<b>Artigo 43º</b>- Na hipótese de dissolução da Igreja, o patrimônio liquido será destinado à Convenção, e, na
sua falta, à Convenção Batista Brasileira, ou outra associação denominacional que venha sucede-la.<br><br>
<b>Artigo 44º</b>- O presente artigo, bem como os artigos 2º, 3º, 15 parágrafo 3º, 35º e parágrafo único e seus
incisos, 36º, 39º e seus incisos, 42º e 43º, só poderão ser alterados, derrogados ou revogados, mediante
homologação da Convenção, através de seu órgão representativo e, e na falta deste, pelo Conselho Geral da
Convenção Batista Brasileira.<br><br>
<b>Artigo 45º</b>- Este Estatuto entrará em vigor após seu registro no 3º Cartório das Pessoas Jurídicas da
Comarca da Capital, e só poderá ser reformado pela Assembléia Geral, especialmente convocada para este fim,
observadas as condições e exigências previstas no artigo 15, parágrafos primeiro e segundo.<br><br>

