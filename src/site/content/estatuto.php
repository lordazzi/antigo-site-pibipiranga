<b class='capitulo'>CAPÍTULO VIII</b>
<i class='capitulo'>DO PATRIMÔNIO E DA RECEITA</i>
<br>

<?php capitulo(); ?>

<hr class='estatuto'>

<b>Artigo 32º</b>- O patrimônio da Igreja é constituído de bens moveis, imóveis e outros compatíveis com sua
